// Operadores Aritiméticos

// multiplicação * 
console.log(3.2 * 5.5)
// divisão /
console.log(12 / 2)
// soma +
console.log(34 + 67)
// subtração -
console.log(69 - 42)

// resto da divisão %
let remainder
remainder = 11 % 3
console.log(remainder)

// incremento ++
let increment = 0
increment++ 
increment++
console.log(increment)

// decremento --
let decrement = 0
console.log(decrement--)
console.log(--decrement)

// exponenncial **
console.log(3 ** 2)

// Grouping operator
let total = (2 + 3) * 5
console.log(total)

// Operadores de comparação

// Irá comparar valores e retornar um Boolean (true/false) como resposta à comparação

let one = 1
let two = 2

// ==   igual a
console.log(two == 1)
console.log(one == "1")

// !=   diferente de
console.log(one != two)
console.log(one != 1)

// ===  estritamente igual a - igual em valor e tipo
console.log(one === "1")
console.log(one === 1)

// !==  estritamente diferente a
console.log(two !== "2")
console.log(two !== 2)

// >    Maior que
console.log(one > two)

// >=   Maior igual a
console.log(one >= 1)
console.log(two >= 1)

// <    Menor que
console.log(one < two)

// <=   Menor igual a
console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)
